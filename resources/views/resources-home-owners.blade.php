            <h2><strong>Sellers Obligation</strong></h2>
            <hr>
            <p><strong>Obligation to Disclose Defects</strong></p>
            <p>Sellers must let buyers know about any material latent defects about their property. A material latent defect is a defect that cannot be discerned through a reasonable inspection of the property. This includes defects that make the property:</p>
            <ul>
            <li>Dangerous or potentially dangerous to the occupants;</li>
            <li>Unfit for living in; or,</li>
            <li>Unfit for the purpose for which the buyer is acquiring it, if the buyer has made this purpose known to the seller.</li>
            </ul>
            <p>Material latent defects may also include:</p>
            <ul>
            <li>a defect that would involve great expense to remedy;</li>
            <li>a circumstance that affects the real estate where a local government or other local authority has given the seller notice, indicating that the circumstance must or should be remedied; or</li>
            <li>a lack of appropriate municipal building and other permits with respect to the property.</li>
            </ul>
            <p>Some examples of material latent defects could be a basement leak, structural damage to the property, water ingress (failure of the building envelope), issues with underground storage tanks, problems with the quality of drinking water and or any damage caused by illegal use of the property.</p>
            <p>New installations or renovations of electrical or gas systems completed without appropriate permits and inspections may also be considered examples of material latent defects. Please contact your local Safety Authority to determine what type of work in a residential property requires gas and electric permits.</p>
            <p>Please note that you may run into legal issues in the future if the new owner discovers problems and material latent defects that you were aware of and did not disclose.</p>

            <h2><strong>Receiving Offers</strong></h2>
            <hr>
            <h4><strong>Offers to Purchase</strong></h4>
            <p>An interested buyer will put forth a written offer to purchase your home, usually through a standard Contract of Purchase and Sale form.</p>
            <p>The Suuty support team can explain to you the process of receiving and reviewing offers through the site as they are all managed by you through your listing. In a traditional setting, your agent would be the one to present offers to you and they would be under an obligation to bring all written offers to you for your consideration. Keep in mind that you are not obligated to accept an offer.</p>
            <p><strong>What Should the Offer contain?</strong></p>
            <p>The written offers will include a number of details that will factor into your decision of which, or any, you will accept.</p>
            <p>These include:</p>
            <ul>
            <li>date of the offer</li>
            <li>full legal names and addresses of both the buyer and the seller</li>
            <li>full legal description of the home</li>
            <li>amount of the deposit</li>
            <li>sale price</li>
            <li>amount of the cash down-payment and details as to how the remainder of the purchase price will be financed or provided</li>
            <li>date for completion of the sale</li>
            <li>date for possession of the home</li>
            <li>subject clauses – a list of the conditions that must be fulfilled before the sale can take place</li>
            <li>chattels – a list of items that re not attached to the building but are included in the sale price</li>
            <li>date and time at which the offer expires</li>
            <li>the signature of the buyer and his or her occupation</li>
            </ul><br>
            <h4><strong>You Have Four Options Once You’ve Received an Offer</strong></h4>
            <p><strong>Accept an Offer Exactly as it Stands</strong></p>
            <p>Consider each offer you receive carefully because once you, the seller, has signed a Contract of Purchase and Sale agreeing to its terms, and your acceptance has been communicated to the buyer, it becomes a legally binding contract.</p>
            <p>This means that both you and the buyer are now bound by the terms of the contract and both must perform your respective obligations as stated. The Contract of Purchase and Sale can be enforced in a court of law. Please refer to our Terms of Use to learn more.</p>
            <p>If you are uncertain about any of the clauses contained in the offer, you may wish to consult a lawyer before signing the contract. If you are unsure of who to get in touch with, the Suuty support team will be able to help connect you with a legal professional.</p>
            <p>However, keep the expiry date of the offer in mind if you decide to postpone acceptance!</p>
            <p><strong>Contact the buyer</strong></p>
            <p>Up to the offer submission end date, you may contact the buyer using Suuty’s direct messaging platform if you would like to request changes to the offer. However, the buyer is not required to make those changes, and once the submission end date has passed, the offer is final.</p>
            <p>The Suuty support team will be able to help should you require our assistance.</p>
            <p><strong>Reject the Offer</strong></p>
            <p>You are not obligated to accept any offer or to make a counter-offer. In a traditional setting, if you were to work with a realtor, there is a chance that you could be legally obligated to pay the commission if you reject an offer that exactly meets all the terms you agreed to in the Listing Contract.</p>

            <p><strong>Ignore the Offer</strong></p>
            <p>You are also under no obligation to acknowledge that you’ve received an offer. Once again, if you were to work with a realtor, you could be legally obligated to pay the commission if you ignore an offer that exactly meets all the terms you agreed to in the Listing Contract.</p>

            <h2><strong>Subject Clauses</strong></h2>
            <hr>
            <p>Subject clauses set out specific conditions that must be met before the sale of the property can go through. One common subject clause that you may encounter is the “Subject to financing” – the buyer makes the sale conditional on finding the amount and type of financing that will enable them to purchase your home.</p>
            <p>Another common clause is “Subject to inspection” – when the buyer makes the purchase conditional upon a satisfactory home inspection.</p>
            <p>By accepting an offer that includes subjects, you must be aware that you are taking your home off the market for the period of time in which the buyers attempt to meet the conditions of the subjects. You should ensure that an agreed upon time for the condition to be met is specified in the offer to purchase.</p>
            <p>If you wish to accept an offer containing a subject clause but still have the freedom to consider other offers, just in case the buyers are unable to remove the condition, you can have the buyer agree to insert a time clause in the contract. This time clause permits you to require the buyer to remove all subject conditions within a short, specified time period, if you receive another offer that you would like to accept. If the buyer does not remove the conditions within that time, the conditional Contract of Purchase and Sale comes to an end. You are now free to accept the second offer.</p>
            <p>For the sale of a strata property, a common clause you may encounter is a Strata Documentation clause, where the buyer makes the counter-offer conditional upon review and approval of all pertinent strata documentation, which can include registered bylaws, rules, strata meeting minutes, financial statements, strata plan, Form B and engineer’s reports.</p>

            <h2><strong>Financing From the Seller's Perspective</strong></h2>
            <hr>
            <p><strong>Existing Financing</strong></p>
            <p>If you currently have a mortgage loan on your home, you may be faced with one of two situations:</p>
            <p><strong>The Buyer Wants to Pay Cash and Has no Mortgage</strong></p>
            <p>In this situation, you will be required to pay out your existing mortgage and there may be an interest penalty for doing so. Having to pay an interest penalty will reduce the price you will be receiving for your home.</p>
            <p><strong>The Buyer Offers to Assume, or Take Over, Your Remaining Mortgage Loan</strong></p>
            <p>In this situation, before agreeing to allow the buyer to assume your mortgage loan, you should check with your mortgage lender to ensure that they will release you from any future obligation to repay the monies owing (if the buyer defaults).</p>
            <p>Contact the financial institution that holds your mortgage to obtain information about your position in both of the above situations. It is useful to do this in advance of listing your home, so that you are able to make decisions with accurate information.</p>
            <p><strong>Financing by the Seller</strong><br>
            If you have no existing mortgage, an offer to pay all cash is ideal and would most likely be your preference. In rare scenarios, the buyer’s offer might state that part of the purchase price is to be paid in cash and part is to be paid over a specific period of time, at a specified interest rate. In effect, the buyer would be asking you to become the lender.</p>
            <p>Please seek legal advice if you are considering an offer containing a request for “seller financing” or a “seller take-back mortgage”, to fully understand the implication of such a financing agreement.</p>


            <h2><strong>Be Cautious and Get Advice</strong></h2>
            <hr>
            <p>Home owners need to be wary of buyers’ financing arrangements in which the sellers assume risks. There is nothing wrong with creative financing if everyone involved is fully aware of the potential risks and consequences but in many cases. The recommendation is that you seek competent advice from legal professionals before finalizing any real estate contract.</p>
            <p>If an offer includes any of the following, be extra cautious:</p>
            <ul>
            <li>no cash paid as a down-payment</li>
            <li>an amount of cash being returned to the buyer</li>
            <li>your equity participation</li>
            <li>a promissory note without a registered mortgage</li>
            <li>an agreement to withhold registering a mortgage</li>
            <li>(you, the seller) securing a new loan before closing the deal on your home</li>
            <li>terms said to be included, but are not written explicitly in the offer</li>
            <li>concealing information from a lending institution</li>
            </ul>

            <h2><strong>Completing the Sale</strong></h2>
            <hr>
            <p>The Contract of Purchase and Sale, which you signed, will state the completion day for the transaction. On that day, legal ownership will transfer from you to the new owner in exchange for the purchase price of the home.</p>
            <p><strong>Do You Need a Lawyer or Notary Public to Complete the Sale?</strong></p>
            <p>Usually, it is the buyer’s lawyer or notary who prepares the documents necessary to transfer the legal ownership but it is recommended that as the seller, you also engage legal counsel to act on your behalf. He or she will protect your interests by:</p>
            <ul>
            <li>checking the documents prepared by the buyer’s lawyer and explaining them to you</li>
            <li>ensuring that your old mortgage has been properly discharged, if this is required</li>
            <li>ensuring that you have no further obligation regarding your old mortgage if it is being assumed by the buyer</li>
            <li>confirming that all payments for which you are responsible have been made</li>
            <li>arranging for you to sign the transfer documents</li>
            <li>preparing a statement for you outlining where all the purchase money was disbursed and giving you the net proceeds of the sale.</li>
            </ul>
            <p><strong>What Costs Can You Expect?</strong></p>
            <ul>
            <li>legal fees to discharge any existing mortgage</li>
            <li>GST on legal fees</li>
            <li>prepayment penalties required by the financial institution for early pay-out of an existing mortgage</li>
            <li>your share of the property taxes for the year if the current year’s taxes have not been paid, plus any penalties due for late payment of unpaid taxes</li>
            </ul>
            <p>The day has arrived!! Now, you have signed the documents, packed your boxes, received your money from the sale of this property and turned over your keys. Congratulations on a successful sale!</p>
